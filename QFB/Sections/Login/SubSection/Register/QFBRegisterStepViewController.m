#import "QFBRegisterStepViewController.h"
#import <WebKit/WebKit.h>

@interface QFBRegisterStepViewController ()<WKNavigationDelegate, WKUIDelegate>

@property (nonatomic, strong) WKWebView *mainWebView;

@end

@implementation QFBRegisterStepViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    self.view.backgroundColor = [UIColor whiteColor];
    [self loadWebView:[NSString stringWithFormat:@"http://regist.fengzhuan.org:8080/payment_union/user/newRegister.action?parent_id=%@&subordinate_brand=%@",_parentId,O_BRAND_ID]];
}

- (void)loadWebView: (NSString *)contentString
{
    if (!contentString) {
        return ;
    }
    NSString *jScript = @"var meta = document.createElement('meta'); meta.setAttribute('name', 'viewport'); meta.setAttribute('content', 'width=device-width'); document.getElementsByTagName('head')[0].appendChild(meta); var imgs = document.getElementsByTagName('img');for (var i in imgs){imgs[i].style.maxWidth='100%';imgs[i].style.height='auto';}";
    WKUserScript *wkUScript = [[WKUserScript alloc] initWithSource:jScript injectionTime:WKUserScriptInjectionTimeAtDocumentEnd forMainFrameOnly:YES];
    
    WKUserContentController *wkUController = [[WKUserContentController alloc] init];
    [wkUController addUserScript:wkUScript];
    WKWebViewConfiguration *wkWebConfig = [[WKWebViewConfiguration alloc] init];
    wkWebConfig.userContentController = wkUController;
    
    WKPreferences *preferences = [WKPreferences new];
    //是否支持JavaScript
    preferences.javaScriptEnabled = YES;
    //不通过用户交互，是否可以打开窗口
    preferences.javaScriptCanOpenWindowsAutomatically = YES;
    wkWebConfig.preferences = preferences;
    
    self.mainWebView = [[WKWebView alloc] initWithFrame:self.view.bounds configuration:wkWebConfig];
    self.mainWebView.scrollView.bounces=YES;
    NSURL *url=[NSURL URLWithString: contentString];
    NSMutableURLRequest *request = [NSMutableURLRequest requestWithURL:url];
    [self.mainWebView loadRequest:request];
    [self.view addSubview:self.mainWebView];
    
    self.mainWebView.navigationDelegate = self;
    self.mainWebView.UIDelegate = self;
    
}

